import random

def jogar():
    mensagem_inicial()
    palavra_secreta =  carrega_palavra_secreta()
    letras_acertadas = ['_' for letras in palavra_secreta]
    print(letras_acertadas)

    enforcou = False
    acertou = False
    erros = 0

    while(not enforcou and not acertou):
        chute = input("Digite uma letra: ").strip().upper()
        if(chute in palavra_secreta):
            carrega_letras_certas(chute, letras_acertadas, palavra_secreta)
        else:
            erros += 1
            desenha_forca(erros)
        enforcou = erros == 7
        acertou = "_" not in letras_acertadas
        print(letras_acertadas)

    if acertou:
        imprime_mesagem_acertou()

    else:
        imprime_mensagem_perdeu(palavra_secreta)

def mensagem_inicial():
    print("*********************************")
    print("***Bem vindo ao jogo da Forca!***")
    print("*********************************")

def carrega_palavra_secreta():
    arquivo =  open("palavras.txt", "r")
    palavras = []
    for linha in arquivo:
       palavras.append(linha.strip())
    arquivo.close()
    palavra_secreta = random.choice(palavras).upper()
    return palavra_secreta

def carrega_letras_certas(chute, letras_acertadas, palavra_secreta):
    index = 0
    for letra in palavra_secreta:
        if (chute.upper() == letra.upper()):
            letras_acertadas[index] = letra
        index += 1

